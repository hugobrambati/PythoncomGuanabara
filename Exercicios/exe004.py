a = input('Digite algo: ')
print(type(a))
print(a.isspace())#retorna em boleano se a variavel e só espaço
print(a.isnumeric())#retorna em boleano se a variavel e só numerico
print(a.isalpha())#retorna em boleano se a variavel e so alfabetico
print(a.isalnum())#retorna em boleano se a variavel e só alfanumerico
print(a.isupper())#retorna em boleano se a variavel esta todo em maisculo
print(a.islower())#retorna em boleano se a variavel esta todo em minusculo
print(a.istitle())#retorna em boleano se a variavel começa com letra maiscula e o restante é  continua com letra minuscula
